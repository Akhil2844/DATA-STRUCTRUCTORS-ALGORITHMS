// BITWISE OPERATORS
#include<iostream>
using namespace std;
int main(){
    int a=7;
    int b=4;

    cout<<"Following are the Bitwise operators"<<endl;
    cout<<(a&b)<<endl;
    cout<<(a|b)<<endl;
    cout<<(~a)<<endl;
    cout<<(a^b)<<endl;

    cout<<"Following are the the shift operators"<<endl;
    cout<<(17<<1)<<endl;
    cout<<(17<<2)<<endl;
    cout<<(19>>1)<<endl;
    cout<<(21>>2)<<endl;

}